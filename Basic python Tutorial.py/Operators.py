#Arithmetic operators
a = 5
b = 10
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)
print(a**b)

#Relational operators(Always gives True or False value)
a = 10
b = 8
print(a==b)
print(a!=b)
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)

#Logical operators(and, or, not)
a = 2
b = 4
print(a>b or b>a)
print(a>b and b>a)
print(not(a>b))

